"""
==  --> value Equality
!=  --> Not Equal to
<   --> less than
>   --> Greater than
<=  --> less than or equal to
>=  --> Greater than or equal to
"""

bool_one = 10 == 10
print(bool_one)

bool_two = 10 == 11
print(bool_two)

not_equal = 10 != 11
print(not_equal)

less_than = 10 < 11
print(less_than)

greater_than = 100 > 50
print(greater_than)

lt_eq = 10 <= 11
print(lt_eq)

gt_eq = 10 >= 9 + 1
print(gt_eq)

gt_eq1 = 10 >= 11 - 1
print(gt_eq1)